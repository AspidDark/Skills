export const getStartingSkillImage = (skillType = 'eag' ):SkillImageModel=>{
    const result = images.find(x=>x.type===skillType&&x.level===1)
    if(!result){
       return images[0]
    }
    return result
}

export const getAll = ():SkillImageModel[] => {
    return images
}

export const getById = (id:string):SkillImageModel => {
    let result = images.find(x=>x.id==id)
    if(!result){
        result = images[0];
    }
    return result
}

export const getSameForOtherLevel = (id:string, isUp:boolean):SkillImageModel => {
    const image = images.filter(x=>x.id===id)[0]
    const resultImages = images.filter(x=>x.type===image.type)
    if(isUp){
         const imageResult =  resultImages.filter(x=>x.level-1===image.level)[0]
        return imageResult
    }
    const imageResult =  resultImages.filter(x=>x.level+1===image.level)[0]
        return imageResult
}

export const getRandomImage = (existingTypes: string []):SkillImageModel =>{
    const notExistingImages = images.filter(x=> {
        if(!existingTypes.includes(x.type)){
            return x
        }
    })

    const firstLevelSkill = notExistingImages.filter(x=>x.level===1)
    const randomImageNumber = getRandomInt(firstLevelSkill.length)
    const randomImage = firstLevelSkill[randomImageNumber]
    return randomImage
}

export const getByType = (type:string):SkillImageModel[] => {
    const result = images.filter(x=>x.type===type)
    return result
}

export const getByLevel = (level:number):SkillImageModel[] =>{
    const result = images.filter(x=> x.level ===level)
    return result
}

let  getRandomInt = (max: number) => {
    return Math.floor(Math.random() * max);
  }
